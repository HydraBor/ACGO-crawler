export function renderFeedbackPrompt({ dailySummaryMarkdown = '' } = {}) {
  const summary = String(dailySummaryMarkdown || '').trim() || '（今日总结暂未填写，请先补充项目根目录的 `今日总结.md`。）';
  return `# 家长反馈生成提示词

你是一名信息学竞赛集训老师。请根据我提供的 ACGO 课堂练习和今日比赛证据，写一段发给家长看的中文反馈。

## 今日总结

以下是老师当天手动填写的课堂总结。生成反馈时必须优先参考它，用它来理解今日学习目标、重难点和易错点。

${summary}

## 输入材料

我会提供以下文件中的一部分或全部：

- \`今日总结.md\`：当天学习目标、重难点、易错点和备注。
- \`作业题目.md\`：课堂练习题面。
- \`比赛题目.md\`：今日比赛题面。
- \`students/学生名-用户ID/课堂练习.md\`：该学生课堂练习每题得分、提交情况和完整代码。
- \`students/学生名-用户ID/今日比赛.md\`：该学生比赛每题得分、提交情况和完整代码。

## 输出要求

- 只输出一段话，不要标题，不要分点，字数约 200 字。
- 反馈面向家长，家长通常不了解信息学竞赛，所以表达要通俗易懂，不要解释证据链，不要堆术语。
- 语气必须亲切、自然、阳光、鼓励、正面，不能有贬低、责备、讽刺或“太菜”等负面表达。
- 同时覆盖“课堂练习情况”和“今日比赛情况”；如果某一部分没有数据，就自然说明该部分暂无记录。
- 必须结合今日总结、每题得分、状态、提交次数、通过时间、完整代码和题目知识点判断，不要编造材料里没有的信息。
- 不要直接逐题报分；对于表现不理想的学生，优先用“完成了哪些题、模拟赛通过了多少题、哪些题还在尝试中”来表达。
- 少用“建议”这种较强的词，可以改成“后续可以”“接下来可以”“之后可以重点练习”等更柔和的说法。
- 重点说明学生今天掌握了什么、还没稳住什么、后续可以加强什么。

请等待我提供某个学生的材料后再生成反馈。`;
}

export function renderBatchInstructions({ hasDailySummary = true } = {}) {
  return `# 批量处理说明

推荐每位学生单独生成一段反馈，便于人工检查后发给家长。

## 推荐流程

1. 先把 \`prompts/家长反馈生成提示词.md\` 发给 AI。
2. 再提供公共材料：\`今日总结.md\`、\`作业题目.md\` 和/或 \`比赛题目.md\`。
3. 对每位学生，依次提供该学生目录下的 \`课堂练习.md\` 和/或 \`今日比赛.md\`。
4. 要求 AI 每位学生只生成一段反馈。
5. 发送给家长前，人工检查姓名、分数、题目表现和练习建议是否准确。

## 材料选择

- 只写课堂反馈：提供 \`今日总结.md\`、\`作业题目.md\` 和该学生的 \`课堂练习.md\`。
- 只写比赛反馈：提供 \`今日总结.md\`、\`比赛题目.md\` 和该学生的 \`今日比赛.md\`。
- 写完整当日反馈：提供 \`今日总结.md\`、两份题面和该学生的两份记录。

${hasDailySummary ? '' : '当前 `今日总结.md` 仍像模板，请先补完今日学习目标、重难点和易错点再批量生成反馈。\n'}

\`raw/summary.json\` 是结构化备份，适合后续接入批量自动生成脚本；日常手动提交给 AI 时优先使用 Markdown 文件。`;
}
